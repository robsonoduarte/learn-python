# Operador AND
print(True and True)
print(True and False)
print(False and True)
print(False and False)

# Operador OR
print(True or True)
print(True or False)
print(False or True)
print(False or False)


# Operador XOR
print(True != True)
print(True != False)
print(False != True)
print(False != False)


# Operadore de Negação
print(not True)
print(not False)
print(not 0)
print(not 1)
print(not not 1)
