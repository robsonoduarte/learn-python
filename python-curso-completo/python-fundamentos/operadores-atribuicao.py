a = 3 # atribuição simples
print(a)

a = a + 7 # atribuição com uma operação
print(a)

a += 5 # atribuição aditiva = a = a + 5
print(a)

a -= 3 # atribuição subtrativa
print(a)

a *= 2 # atribuição multiplicativa
print(a)

a /= 4 # atribuição divisiva
print(a)


a %= 4 # atribuição modular
print(a)

a **= 8 # atribuição com potenciação
print(a)

a //= 255 # atribuição com divisão inteira
print(a)